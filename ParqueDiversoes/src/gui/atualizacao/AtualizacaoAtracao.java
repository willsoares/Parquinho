    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jTextField1.getText().trim() != "" && jTextField2.getText().trim() != "" && jTextField3.getText().trim() != ""
                && jTextField4.getText().trim() != "") {

            this.atracao.setNome(jTextField1.getText().toString());
            this.atracao.setTipo(jTextField2.getText().toString());
            this.atracao.setDataManutencao(Date.valueOf(jTextField3.getText().toString()));
            this.atracao.setStatus(Integer.valueOf(jTextField4.getText().toString()));

            try {
                bDController.atualizaAtracao(atracao);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(AtualizacaoProduto.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(AtualizacaoProduto.class.getName()).log(Level.SEVERE, null, ex);
            }

            this.dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

