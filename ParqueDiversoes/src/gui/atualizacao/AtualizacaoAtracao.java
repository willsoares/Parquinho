    public void preencheCampos(int id) throws ClassNotFoundException, SQLException {
        this.bDController = new BDController();
        this.atracao = bDController.selectAtracao(id);

        jTextField1.setText(atracao.getNome());
        jTextField2.setText(atracao.getTipo());
        jTextField3.setText(atracao.getDataManutencao().toString());
        jTextField4.setText(String.valueOf(atracao.getStatus()));
    }

