    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jTextField1.getText().trim() != "" && jTextField2.getText().trim() != "" && jTextField3.getText().trim() != "") {
            
            this.loja.setCategoria(jTextField1.getText().toString());
            this.loja.setNome(jTextField2.getText().toString());
            this.loja.setLocal(jTextField3.getText().toString());
            
            try {
                this.bDController.atualizaLoja(loja);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(AtualizacaoLoja.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(AtualizacaoLoja.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            this.dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

