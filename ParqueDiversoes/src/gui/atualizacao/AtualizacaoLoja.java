    public void preencheCampos(int id) throws ClassNotFoundException, SQLException {
        this.bDController = new BDController();
        this.loja = bDController.selectLoja(id);
                
        jTextField1.setText(loja.getCategoria());
        jTextField2.setText(loja.getNome());
        jTextField3.setText(loja.getLocal());
    }
